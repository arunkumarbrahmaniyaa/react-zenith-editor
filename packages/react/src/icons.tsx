import React from 'react';

/**
 * Lightweight, dependency-free icon set.
 *
 * These replace `lucide-react` so the library ships with zero runtime icon
 * dependencies. Each icon is a minimal 24x24 stroked SVG that mirrors the
 * `{ size, strokeWidth, style }` API previously consumed from lucide.
 */

export type IconProps = {
  size?: number;
  strokeWidth?: number;
  style?: React.CSSProperties;
  className?: string;
};

const createIcon = (children: React.ReactNode) => {
  const Icon: React.FC<IconProps> = ({ size = 24, strokeWidth = 2, style, className }) => (
    <svg
      xmlns="http://www.w3.org/2000/svg"
      width={size}
      height={size}
      viewBox="0 0 24 24"
      fill="none"
      stroke="currentColor"
      strokeWidth={strokeWidth}
      strokeLinecap="round"
      strokeLinejoin="round"
      style={style}
      className={className}
      aria-hidden="true"
      focusable="false"
    >
      {children}
    </svg>
  );
  return Icon;
};

